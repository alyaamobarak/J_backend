const chatResponses = {
  "استعلام عن إعدادات الحساب": "تقدر تغير إعداداتك من حسابي > الإعدادات.",
  "الدفع والتقسيط": "عندك إمكانية للدفع عند الاستلام أو بالفيزا أو التقسيط عبر ساهولة.",
  "استفسار متعلق بالطلب": "برجاء كتابة رقم الطلب وسنساعدك فورًا.",
  "استفسار عام آخر": "من فضلك وضح سؤالك."
};

exports.handleChatMessage = (req, res) => {
  const { message } = req.body;
  const response = chatResponses[message] || "نعتذر، لم نفهم سؤالك. برجاء المحاولة بصيغة أوضح.";
  res.json({ response });
};
